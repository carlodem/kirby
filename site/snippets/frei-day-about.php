<h1 class="title">Was ist der FREI DAY?</h1>
<hr class="divider" align="left">
</hr>
<p class="description">Der FREI DAY ist ein Lernformat, in dem das Leben die Fragen stellt. Schüler*innen sind selbst gewählten Zukunftsfragen auf der Spur. Sie entwickeln innovative und konkrete Lösungen und setzen ihre Projekte direkt in der Nachbarschaft und Gemeinde um. Der FREI DAY ist ein Lernformat, das Schüler*innen dazu befähigt, die Herausforderungen unserer Zeit selbst anzupacken und diesen mit Mut, Verantwortungsbewusstsein und Kreativität zu begegnen. Am FREI DAY lernen Kinder und Jugendliche, die Welt zu verändern.</p>

            
